let _uiLang = 'en';

function setUiLang(lang) {
  _uiLang = STRINGS[lang] ? lang : 'en';
}

// Look up a chrome string; falls back en -> ms -> key. Supports {n}-style params.
function t(key, params) {
  const table = STRINGS[_uiLang] || STRINGS.en;
  let s = table[key];
  if (s == null) s = (STRINGS.en[key] != null ? STRINGS.en[key] : (STRINGS.ms[key] != null ? STRINGS.ms[key] : key));
  if (params) {
    Object.keys(params).forEach(k => { s = s.replace('{' + k + '}', params[k]); });
  }
  return s;
}

// Apply chrome strings to all tagged static elements for the given language.
function applyChrome(lang) {
  setUiLang(lang);
  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(eln => {
    eln.textContent = t(eln.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(eln => {
    eln.setAttribute('placeholder', t(eln.getAttribute('data-i18n-placeholder')));
  });
  document.querySelectorAll('[data-i18n-aria-label]').forEach(eln => {
    eln.setAttribute('aria-label', t(eln.getAttribute('data-i18n-aria-label')));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(eln => {
    eln.setAttribute('title', t(eln.getAttribute('data-i18n-title')));
  });
  document.querySelectorAll('[data-i18n-text]').forEach(eln => {
    eln.dataset.text = t(eln.getAttribute('data-i18n-text'));
  });
}
